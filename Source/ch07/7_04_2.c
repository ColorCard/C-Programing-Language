/*【例7-4-2】输入一个正整数n (1≤n≤10)，再输入n个整数，将它们存入数组a中。
① 输出最小值和它所对应的最小下标。
② 将最小值与第一个数交换，输出交换后的n个数。*/

/* 将数组的最小值与第一个数交换 */
# include <stdio.h>

# define MAXN 10                    /* 定义符号常量MAXN */

int main() {
    int i, index, n;
    int a[MAXN];

    printf("Enter n: ");                 /* 提示输入n */
    scanf("%d", &n);
    printf("Enter %d integers: ", n);    /* 提示输入n 个数 */

    for (i = 0; i < n; i++) {
        scanf("%d", &a[i]);
    }

    /* 找最小值a[index] */
    index = 0;                    /* 假设a[0]是最小值，即下标为0的元素最小 */
    for (i = 1; i < n; i++) {
        if (a[i] < a[index]) {    /* 如果 a[i] 比假设的最小值还小 */
            index = i;            /* 再假设 a[i] 是新的最小值，即下标为 i 的元素最小 */
        }
    }

    /* 输出最小值和对应的下标 */
    printf("min is %d\tsub is %d\n", a[index], index);   /* 第23行 */

    {
        int temp;                /* 在复合语句中定义变量temp*/
        temp = a[index];         /* 以下3句交换a[index]和a[0] */
        a[index] = a[0];
        a[0] = temp;

        for (i = 0; i < n; i++) {
            printf(" %d ", a[i]);
        }
    }

    return 0;
}